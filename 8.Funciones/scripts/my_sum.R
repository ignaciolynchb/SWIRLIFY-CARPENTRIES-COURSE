#my_sum es el nombre que recibe la función
#a y b son los inputs que recibe la función
my_sum <- function(a, b) { 
  the_sum <- a + b #aquí se escribe el cuerpo de la función
#en este caso, se almacena en the_sum el valor resultante de hacer a+b
  return(the_sum) #por ultimo, se especifica a la función cuál es el output, en este caso, the sum
}

